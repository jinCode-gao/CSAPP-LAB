/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
  int sign = 0x1<<31;
  int upperBound = ~(sign|0x39);
  int lowerBound = ~0x30;
  upperBound = sign&(upperBound+x)>>31;
  lowerBound = sign&(lowerBound+1+x)>>31;
  return !(upperBound|lowerBound);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  x = !!x;
  x = ~x+1;//如果x=0，这里x变为0；如果x！=0，这里x变为-1，即全1
  return (x&y)|(~x&z);
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x,int y){
  //取x y符号
  int x_sign=x>>31;
  x_sign=x_sign&0x00000001;//x为正,x_sign为0
  int y_sign=y>>31;
  y_sign=y_sign&0x00000001;
  int flag=x_sign^y_sign;//xy同号，flag=0

  //计算y-x
  int nx=~x+1;
  int yx=y+nx;//y-x
  int yx_sign=yx>>31;
  yx_sign=yx_sign&0x00000001;//y-x为正,yx_sign为0

  return ((!flag)&(!yx_sign))|((flag)&x_sign);//分xy同号和xy异号两种情况，同号则看两者差的符号，异号看x是正是负
}

