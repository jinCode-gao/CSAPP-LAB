/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
 int bitXor(int x, int y) {
  int p = ~x & y;//p中的1代表x是0，y是1的
  int q = x & ~y;//q中的1代表y是0，x是1的
  //现在需要qp中的1都保留，但是不能用|
  int r = ~(~p & ~q);//~p和~q后，pq中需要保留的就是0，二者&就可以保留所有的0，最后取~
  return r;
}
